def mprint(*args, **kwargs):
    """
    Print only from rank 0.
    """
    try:
        if dist.get_rank() == 0:
            print(*args, **kwargs)
    except:
        print(*args, **kwargs)

def cleanup():
    """
    End DDP training.
    """
    dist.barrier()
    mprint("Done!")
    dist.barrier()
    dist.destroy_process_group()


